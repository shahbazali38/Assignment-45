"use strict";
// Array of favorite pizzas
let pizzas = ['Pepperoni', 'Margherita', 'BBQ Chicken'];
// Print the name of each pizza
console.log("My favorite pizzas are:");
for (let i = 0; i < pizzas.length; i++) {
    console.log(pizzas[i]);
}
// Print a sentence using the name of each pizza
console.log("\nStatements about my favorite pizzas:");
for (let i = 0; i < pizzas.length; i++) {
    console.log(`I like ${pizzas[i]} pizza.`);
}
// Statement about how much I like pizza
console.log("\nI really love pizza!");
